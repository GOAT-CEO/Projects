def play_animation(frames, delay=0.1, repeat=10):
    for _ in range(repeat):
        # Play frames forward
        for frame in frames:
            clear_screen()
            print(frame)
            time.sleep(delay)

        # Play frames in reverse
        for frame in reversed(frames):
            clear_screen()
            print(frame)
            time.sleep(delay)








